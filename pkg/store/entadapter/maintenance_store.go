// ============================================================================
// Conversions
// ============================================================================

func entMaintenanceOpToStore(e *ent.MaintenanceOperation) *store.MaintenanceOperation {
	return &store.MaintenanceOperation{
		ID:          e.ID.String(),
		Key:         e.Key,
		Title:       e.Title,
		Description: e.Description,
		Category:    e.Category,
		Status:      e.Status,
		CreatedAt:   e.Created,
		StartedAt:   e.StartedAt,
		CompletedAt: e.CompletedAt,
		StartedBy:   e.StartedBy,
		Result:      e.Result,
		Metadata:    e.Metadata,
	}
}

func entMaintenanceRunToStore(e *ent.MaintenanceOperationRun) *store.MaintenanceOperationRun {
	return &store.MaintenanceOperationRun{
		ID:           e.ID.String(),
		OperationKey: e.OperationKey,
		Status:       e.Status,
		StartedAt:    e.StartedAt,
		CompletedAt:  e.CompletedAt,
		StartedBy:    e.StartedBy,
		Result:       e.Result,
		Log:          e.Log,
	}
}

// ============================================================================
// Seed
// ============================================================================

// SeedMaintenanceOperations inserts the built-in operations and migrations that
// are missing from the store. It is idempotent and replaces the SQLite
// randomblob() UUID seeds with Go-side ids generated by the Ent default.
func (s *MaintenanceStore) SeedMaintenanceOperations(ctx context.Context) error {
	for _, op := range defaultSeedOperations {
		exists, err := s.client.MaintenanceOperation.Query().
			Where(maintenanceoperation.KeyEQ(op.Key)).
			Exist(ctx)
		if err != nil {
			return err
		}
		if exists {
			continue
		}
		// ID is intentionally left unset so the Ent schema default (uuid.New)
		// mints it — the Go replacement for the SQLite randomblob() seed.
		err = s.client.MaintenanceOperation.Create().
			SetKey(op.Key).
			SetTitle(op.Title).
			SetDescription(op.Description).
			SetCategory(op.Category).
			SetStatus(store.MaintenanceStatusPending).
			Exec(ctx)
		if err != nil && !ent.IsConstraintError(err) {
			return err
		}
	}
	return nil
}

// ============================================================================
// Maintenance Operation Operations
// ============================================================================

// ListMaintenanceOperations returns all registered operations and migrations.
func (s *MaintenanceStore) ListMaintenanceOperations(ctx context.Context) ([]store.MaintenanceOperation, error) {
	entities, err := s.client.MaintenanceOperation.Query().
		Order(
			maintenanceoperation.ByCategory(),
			maintenanceoperation.ByCreated(),
		).
		All(ctx)
	if err != nil {
		return nil, err
	}
	ops := make([]store.MaintenanceOperation, 0, len(entities))
	for _, e := range entities {
		ops = append(ops, *entMaintenanceOpToStore(e))
	}
	return ops, nil
}

// GetMaintenanceOperation returns a single operation by key.
func (s *MaintenanceStore) GetMaintenanceOperation(ctx context.Context, key string) (*store.MaintenanceOperation, error) {
	e, err := s.client.MaintenanceOperation.Query().
		Where(maintenanceoperation.KeyEQ(key)).
		Only(ctx)
	if err != nil {
		return nil, mapError(err)
	}
	return entMaintenanceOpToStore(e), nil
}

// UpdateMaintenanceOperation updates an operation's status and result fields.
func (s *MaintenanceStore) UpdateMaintenanceOperation(ctx context.Context, op *store.MaintenanceOperation) error {
	update := s.client.MaintenanceOperation.Update().
		Where(maintenanceoperation.KeyEQ(op.Key)).
		SetStatus(op.Status).
		SetStartedBy(op.StartedBy).
		SetResult(op.Result).
		SetMetadata(op.Metadata)

	if op.StartedAt != nil {
		update.SetStartedAt(*op.StartedAt)
	} else {
		update.ClearStartedAt()
	}
	if op.CompletedAt != nil {
		update.SetCompletedAt(*op.CompletedAt)
	} else {
		update.ClearCompletedAt()
	}

	n, err := update.Save(ctx)
	if err != nil {
		return mapError(err)
	}
	if n == 0 {
		return store.ErrNotFound
	}
	return nil
}

