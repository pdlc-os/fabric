// Maintenance Operation Run Operations
// ============================================================================

// CreateMaintenanceRun inserts a new run record.
func (s *MaintenanceStore) CreateMaintenanceRun(ctx context.Context, run *store.MaintenanceOperationRun) error {
	create := s.client.MaintenanceOperationRun.Create().
		SetOperationKey(run.OperationKey).
		SetStatus(run.Status).
		SetLog(run.Log)

	if run.ID != "" {
		uid, err := parseUUID(run.ID)
		if err != nil {
			return err
		}
		create.SetID(uid)
	}
	if !run.StartedAt.IsZero() {
		create.SetStartedAt(run.StartedAt)
	}
	if run.CompletedAt != nil {
		create.SetCompletedAt(*run.CompletedAt)
	}
	if run.StartedBy != "" {
		create.SetStartedBy(run.StartedBy)
	}
	if run.Result != "" {
		create.SetResult(run.Result)
	}

	created, err := create.Save(ctx)
	if err != nil {
		return mapError(err)
	}
	run.ID = created.ID.String()
	run.StartedAt = created.StartedAt
	if run.Status == "" {
		run.Status = created.Status
	}
	return nil
}

// UpdateMaintenanceRun updates a run's status, result, and log.
func (s *MaintenanceStore) UpdateMaintenanceRun(ctx context.Context, run *store.MaintenanceOperationRun) error {
	uid, err := parseUUID(run.ID)
	if err != nil {
		return err
	}

	update := s.client.MaintenanceOperationRun.Update().
		Where(maintenanceoperationrun.IDEQ(uid)).
		SetStatus(run.Status).
		SetResult(run.Result).
		SetLog(run.Log)

	if run.CompletedAt != nil {
		update.SetCompletedAt(*run.CompletedAt)
	} else {
		update.ClearCompletedAt()
	}

	n, err := update.Save(ctx)
	if err != nil {
		return mapError(err)
	}
	if n == 0 {
		return store.ErrNotFound
	}
	return nil
}

// GetMaintenanceRun returns a single run by ID.
func (s *MaintenanceStore) GetMaintenanceRun(ctx context.Context, id string) (*store.MaintenanceOperationRun, error) {
	uid, err := parseGetID(id)
	if err != nil {
		return nil, err
	}
	e, err := s.client.MaintenanceOperationRun.Get(ctx, uid)
	if err != nil {
		return nil, mapError(err)
	}
	return entMaintenanceRunToStore(e), nil
}

// ListMaintenanceRuns returns runs for a given operation key, ordered by
// started_at DESC.
func (s *MaintenanceStore) ListMaintenanceRuns(ctx context.Context, operationKey string, limit int) ([]store.MaintenanceOperationRun, error) {
	if limit <= 0 {
		limit = 20
	}
	entities, err := s.client.MaintenanceOperationRun.Query().
		Where(maintenanceoperationrun.OperationKeyEQ(operationKey)).
		Order(maintenanceoperationrun.ByStartedAt(entsql.OrderDesc())).
		Limit(limit).
		All(ctx)
	if err != nil {
		return nil, err
	}
	runs := make([]store.MaintenanceOperationRun, 0, len(entities))
	for _, e := range entities {
		runs = append(runs, *entMaintenanceRunToStore(e))
	}
	return runs, nil
}

// AbortRunningMaintenanceOps transitions any "running" operation runs to
// "failed" and resets stalled "running" migrations back to "pending" so they
// can be retried. Called at server startup to clean up operations interrupted
// by a restart.
func (s *MaintenanceStore) AbortRunningMaintenanceOps(ctx context.Context) (int64, int64, error) {
	now := time.Now()

	runs, err := s.client.MaintenanceOperationRun.Update().
		Where(maintenanceoperationrun.StatusEQ(store.MaintenanceStatusRunning)).
		SetStatus(store.MaintenanceStatusFailed).
		SetCompletedAt(now).
		SetResult(abortResult).
		Save(ctx)
	if err != nil {
		return 0, 0, err
	}

	migrations, err := s.client.MaintenanceOperation.Update().
		Where(
			maintenanceoperation.StatusEQ(store.MaintenanceStatusRunning),
			maintenanceoperation.CategoryEQ(store.MaintenanceCategoryMigration),
		).
		SetStatus(store.MaintenanceStatusPending).
		ClearStartedAt().
		ClearCompletedAt().
		SetResult(abortResult).
		Save(ctx)
	if err != nil {
		return int64(runs), 0, err
	}

	return int64(runs), int64(migrations), nil
}
