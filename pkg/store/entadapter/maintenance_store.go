// MaintenanceStore implements store.MaintenanceStore using the Ent ORM.
type MaintenanceStore struct {
	client *ent.Client
}

// NewMaintenanceStore creates a new Ent-backed MaintenanceStore.
func NewMaintenanceStore(client *ent.Client) *MaintenanceStore {
	return &MaintenanceStore{client: client}
}

// abortResult is the result message stamped on operations/runs that were
// interrupted by a server restart. Kept identical to the SQLite backend.
const abortResult = `{"error":"aborted: server was restarted while operation was running"}`

